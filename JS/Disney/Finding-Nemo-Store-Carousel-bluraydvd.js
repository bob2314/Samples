function linkOut(type, link, largeImage, trackingSection, trackingId)
{
	switch (type) {

		case "external":
			trackLink({ id : trackingSection + trackingId });
			window.open(unescape(link), "_blank");
			break;

		case "internal":
			window.location = unescape(link);
			break;
			
		case "internalCustom":
			window.location = unescape(link);
			break;
			
		default:
			break;
	}
};


