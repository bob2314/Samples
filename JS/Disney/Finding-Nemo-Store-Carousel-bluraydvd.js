////////////////////////////////////////////////////////////////////////////////////////////////////
//	Carousel
////////////////////////////////////////////////////////////////////////////////////////////////////

function Carousel(obj) {
	//defaults
	this.options = {
		thumb_width: 140,
		thumb_height: 120,
		content : null,
		page : "",
		root : "",
		contentId : "carouselContent",
		margin : [4, 20, 0, 10],
		tagId : "carouselTags",
		availableTags : []
	}

	$.extend(this.options, obj);

	var self = this;
	this.content = this.options.content;
	this.contentId = this.options.contentId;
	this.active = this.content;
	this.inactive = [];
	this.leftPan = 0;
	this.currentTag = "";
	this.currentLast = 0; //figured out in setup

	this.currentPageNum = 0;
	this.totalPageNum = 1;
	this.carouselIsAnimating = false;

	this.setup = function() {
		self.totalPages = Math.ceil(self.active.length/4);
		var html = "";
		if (self.options.availableTags.length > 0) {
		
			$(".carouselBackground").css("display", "block");

			html += '<div class="carouselTagAllOff" id="tag_all" onclick="' + self.options.id + '.filterByTag(\'\')">All</div>';
			for(var i = 0; i < self.options.availableTags.length; i++) {
				html += '<div class="carouselTag" id="tag_'+ self.options.availableTags[i].replace(" ","_") +'" onclick="' + self.options.id + '.filterByTag(\'' + self.options.availableTags[i] + '\')">' + self.options.availableTags[i] + '</div>';
			}
			html += '<div class="carouselTag" id="tag_grid_view" onclick="gridView();">Grid View&nbsp;&nbsp;&nbsp; <div id="gridViewIcon"></div></div>';
			$("#"+ self.options.tagId).html(html);

			nemoButton("carouselOn", "#tag_all", "carouselAll", 50, "All", undefined);
			for(var i = 0; i < self.options.availableTags.length; i++) {
				nemoButton("carouselOff", "#tag_" + self.options.availableTags[i].replace(" ", "_"), "carouselTag" + i, 120, self.options.availableTags[i], undefined);
			}
			nemoButton("carouselOff", "#tag_grid_view", "carouselGridView", 130, "Grid View", "gridView");
		}

		self.showPagination();

		//carousel Item hover
		$("#carouselContent .carouselItem").live({
			mouseenter:
			function()
			{
				$(this).css({
					color: "#F9D24A" // carousel item text
				});
			},
			mouseleave:
			function()
			{
				$(this).css({
					color: "white"
				});
			}
		});

		//grid content hover states
		$("#gridViewContent .carouselItem").live({
			mouseenter:
			function()
			{
				$(this).css({
					color: "#B3BCCF" // This is the text color in the gridview
				});
			},
			mouseleave:
			function()
			{
				$(this).css({
					color: "#002165"
				});
			}
		});
	}


	this.gridViewContent = function() {
		//run through tags.
		var tags = this.options.availableTags;
		html = "";
		for (var j = 0; j < tags.length; j++) {
			var arr = [];
			html += '<div class="heading">' + tags[j] + '</div>'; // Section Heading
			for (var i = 0; i < self.content.length; i++) {
				if ($.inArray(tags[j], self.content[i].tags) != -1) {
					html += self.singleThumbHtml(self.content[i],"_grid");
				}
			}
		}
		return html;
	}


	this.filterByTag = function(tagName) {

		// reset buttons
		nemoButton("carouselOff", "#tag_all", "carouselAll", 50, "All", undefined);
		for(var i = 0; i < self.options.availableTags.length; i++) {
			nemoButton("carouselOff", "#tag_" + self.options.availableTags[i], "carousel_tag" + i, 120, self.options.availableTags[i], undefined);
		}
		nemoButton("carouselOff", "#tag_grid_view", "carouselGridView", 130, "Grid View", "gridView");


		// change just the selected one
		if (tagName == "") nemoButton("carouselOn", "#tag_all", "carouselAllOn", 50, "All", undefined);
		else if (tagName == "Grid View") html += nemoButton("carouselOn", "#tag_grid_view", "carouselGridView", 130, "Grid View", "gridView");
		else html += nemoButton("carouselOn", "#tag_" + tagName, "carousel_tag_selected", 120, tagName, undefined);

		self.active = [];
		self.inactive = [];
		if (tagName != "") {
			for (var i = 0; i < self.content.length; i++) {
				if ($.inArray(tagName, self.content[i].tags) != -1) {
					self.active.push(self.content[i]);
				}
				else {
					self.inactive.push(self.content[i]);
				}
			}
		}
		else {
			self.active = self.content;
		}
		self.update();

		return self.active;

	}


	this.update = function() {
		for (var i = 0; i < self.active.length; i++) {
			$("#" + self.active[i].id + "_container").show();
		}
		for (i = 0; i < self.inactive.length; i++) {
			$("#" + self.inactive[i].id + "_container").hide();
		}

		self.currentPageNum = 0;
		self.totalPageNum = 0;
		self.showPagination();

		//set left to 0px;
		if (self.leftPan != 0)
		{
			self.panHome();
		}
	}


	this.showPagination = function()
	{
		// global vars
		var html = '';
		var currentNumPages = Math.ceil(self.active.length / 4);
		self.totalPageNum = currentNumPages;

		if (self.active.length <= 4)
		{
			// hide the pagination block
			self.currentLast = self.active.length;
			$(".paginationBlock").css("display", "none");
		}
		else
		{
			// show the pagination block
			self.currentLast = 4;


			// add buttons to pagination block
			html = '<div class="carouselPaginationButton" id="carouselPaginationArrowLeft"></div>';
			for (var a = 0; a < currentNumPages; a++)
			{
				html += '<div class="carouselPaginationButton" id="carouselPaginationButton_' + a + '"></div>';
			}
			html += '<div class="carouselPaginationButton" id="carouselPaginationArrowRight"></div>';
			$(".paginationBlock").empty().html(html);
			$(".paginationBlock").css("display", "none");
			
			
			// align pagination block to center
			$(".paginationBlock").css({
				"width" : $(".carouselPaginationButton").width() * (currentNumPages + 2)
			});

			$(".paginationBlock").css({
				"left" : $(".carousel").width() / 2 - $(".paginationBlock").width() / 2
			});
			
			$(".paginationBlock").css("display", "block");


			// add listeners to buttons
			$("#carouselPaginationArrowLeft").click(function() {
				self.panTo("left");
			});

			$("#carouselPaginationArrowRight").click(function() {
				self.panTo("right");
			});

			for (var b = 0; b < currentNumPages; b++)
			{
				$("#carouselPaginationButton_" + b).click(function(anon) {
					return function() {
						self.panTo(anon);
					}
				}(b));
			}


			// update pagination
			self.updatePagination();
		}
	}


	this.panTo = function(value)
	{
		if (self.carouselIsAnimating) return false;

		if (value == "left")
		{
			if (self.currentPageNum != 0)
			{
				self.currentPageNum --;
			}
		}
		else if (value == "right")
		{
			if (self.currentPageNum != self.totalPageNum - 1)
			{
				self.currentPageNum ++;
			}
		}
		else
		{
			self.currentPageNum = value;
		}

		self.updatePagination();
		self.pan();
	}


	this.pan = function() {

		if (self.carouselIsAnimating) return false;

		var panAmount = 4 * (self.options.thumb_width + self.options.margin[1] + self.options.margin[3]);
		self.leftPan = -(self.currentPageNum * panAmount);
		self.carouselIsAnimating = true;
		
		if (is3DTransforms)
		{
			TweenMax.to($("#" + self.contentId), 1.0, {
				css : { 
					left : self.leftPan,
					transformOrigin : "left top"
				},
				ease : Expo.easeInOut,
				onComplete : function() {
					self.carouselIsAnimating = false;
				}
			});
		}
		else
		{
			$("#" + self.contentId).animate({
				avoidTransforms : true,
				left : self.leftPan
			}, 1000, function() {
				self.carouselIsAnimating = false;
			});
		}
	}


	this.panHome = function() {

		if (self.active.length >= 4)
		{
			self.currentLast = 4;
		}
		else
		{
			self.currentLast = self.active.length;
		}

		self.leftPan = 0;
		self.carouselIsAnimating = true;
		self.currentPageNum = 0;
		
		if (is3DTransforms)
		{
			TweenMax.to($("#" + self.contentId), 1.0, {
				css : { 
					left : self.leftPan,
					transformOrigin : "left top"
				},
				ease : Expo.easeInOut,
				onComplete : function() {
					self.carouselIsAnimating = false;
				}
			});
		}
		else
		{
			$("#" + self.contentId).animate({
				left : self.leftPan
			}, 1000, function() {
				self.carouselIsAnimating = false;
			});
		}

		return self.leftPan;
	}


	this.updatePagination = function()
	{
		var currentNumPages = Math.ceil(self.active.length / 4);
		setBackgroundImage("#carouselPaginationArrowLeft", "buttons/button_spritesheet.png", "no-repeat 0px -244px");
		setBackgroundImage("#carouselPaginationArrowRight", "buttons/button_spritesheet.png", "no-repeat -26px -244px");
		for (var a = 0; a < currentNumPages; a++)
		{
			setBackgroundImage("#carouselPaginationButton_" + a, "buttons/button_spritesheet.png", "no-repeat -84px -244px");
		}

		setBackgroundImage("#carouselPaginationButton_" + self.currentPageNum, "buttons/button_spritesheet.png", "no-repeat -56px -244px");
	}


	this.populate = function() {
		var html = '';
		for (var i=0; i < self.content.length; i++) {
			html += self.singleThumbHtml(self.content[i]);
		}
		$("#"+ self.contentId).html(html);
		return html;
	}


	this.singleThumbHtml = function(content, append) {
		append = append || "";
		var innerStyles = 'width:' + self.options.thumb_width + 'px; height:' + self.options.thumb_height +'px; background: url(' + mediaRoot + content.image + ')';
		var outerStyles = 'width:' + self.options.thumb_width + 'px; margin:' + self.options.margin[0] +  'px ' + self.options.margin[1] +	'px ' + self.options.margin[2] + 'px ' + self.options.margin[3] + 'px;';
		var html = '';

		if (content.linkType == "internal") {
			html += '<div class="carouselItem" id="' + content.id + '_container' + append + '" style="' + outerStyles + '" onclick="linkOut(\'' + content.linkType + '\',\''+ self.options.root + content.link + '?id=' + content.id + '\');">'
		}
		else if (content.linkType == "internalCustom") {
			html += '<div class="carouselItem" id="' + content.id + '_container' + append + '" style="' + outerStyles + '" onclick="linkOut(\'' + content.linkType + '\',\''+ self.options.root + content.link +  '\');">'
		}
		else if (content.linkType == "external") {
			html += '<div class="carouselItem" id="' + content.id + '_container' + append + '" style="' + outerStyles + '" onclick="linkOut(\'' + content.linkType + '\',\''+ escape(content.link) + '\',\''+ content.largeImage + '\',\''+ content.trackingSection + '\',\'' + content.tracking+'\');">'
		}

		html += '<div class="carouselItemGlow"></div>';
		html += '<div class="carouselImage" id="' + content.id + append + '" style="' + innerStyles + '"></div>';
		if (content.name != "") {
			html += '<br/><br/>' + content.name;
		}
		html += "</div>";

		return html;
	}


	//still init
	self.setup();
	self.populate();
};


